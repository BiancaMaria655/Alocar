    public void  salvar() throws ParseException, ParseException, SQLException{
        try{
            Cliente cli = new Cliente();
            cli.setNomeCli(jTextFieldNome.getText());
            cli.setCpfCli(Long.parseLong(jTextFieldCPF.getText()));
            cli.setRgCli(jTextFieldRG.getText());
            cli.setTelefoneSMSCli(jTextFieldSMS.getText());
            cli.setTelefone2Cli(jTextFieldtel2.getText());
            cli.setEmailCli(jTextFieldEmail.getText());
            cli.setEnderecoCli(jTextFieldEndereco.getText());
            cli.setBairroCli(jTextFieldBairro.getText());
            cli.setCidadeCli(jTextFieldCidade.getText());
            cli.setUfCli(jTextFieldUF.getText());
            cli.setCepCli(jTextFieldCEP.getText());
            cli.setNumeroCNHCli(jTextFieldNCNH.getText());
            //String[] dataSep = (jFormattedTextFieldValidade.getText()).split("/");
            //LocalDate data = LocalDate.of(Integer.parseInt(dataSep[2]), Integer.parseInt(dataSep[1]), Integer.parseInt(dataSep[0]));
            SimpleDateFormat formato = new SimpleDateFormat("YYYY-MM-DD");
            String data = jFormattedTextFieldValidade.getText();
            Date parseData =  formato.parse(data);
            cli.setValidadeCNHCli((java.sql.Date) parseData);
            cli.setCategoriaCli(jTextFieldCategoria.getText());

            ClienteDAO dao = new ClienteDAO();
            dao.novo(cli);
            JOptionPane.showMessageDialog(null, "Dados salvos com sucesso!\n\n");
        }catch(SQLException e){
            JOptionPane.showMessageDialog(null, "Erro ao salvar dados\n\n" + e);
        }
    }
