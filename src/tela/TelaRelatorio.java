      public void exibirDados() throws SQLException {
        
        DefaultTableModel modelo = (DefaultTableModel) tabelaLoc.getModel();
        modelo.setNumRows(0);
        LocacaoDAO locacao = new LocacaoDAO();

        for (Locacao loc : locacao.listar()) {

            modelo.addRow(new Object[]{
                loc.getIdLoc(),
                loc.getQtdDias_locacao(),
                loc.getValorTotal_locacao()
            });

        }

    }
