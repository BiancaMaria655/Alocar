public void pesquisarLoc(int chave) throws SQLException {
        
        DefaultTableModel modelo = (DefaultTableModel) tabelaLoc.getModel();
        modelo.setNumRows(0);
        LocacaoDAO locacao = new LocacaoDAO();

        for (Locacao loc : locacao.pesquisar(chave)) {

            modelo.addRow(new Object[]{
                loc.getIdLoc(),
                loc.getQtdDias_locacao(),
                loc.getValorTotal_locacao()
            });
        }
    }
